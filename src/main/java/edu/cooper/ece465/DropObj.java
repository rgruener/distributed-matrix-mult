package edu.cooper.ece465;

public class DropObj {
    public Double [] row;
    public Double [] col;
    public int rowNum;
    public int colNum;

    public DropObj(Double[] row, Double[] col, int rowNum, int colNum) {
        this.row = row;
        this.col = col;
        this.rowNum = rowNum;
        this.colNum = colNum;
    }
}
